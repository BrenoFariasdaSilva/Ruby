# Ruby Math Operators

# Addition
puts("1 + 1 = #{1 + 1}")

# Subtraction
puts("1 - 1 = #{1 - 1}")

# Multiplication
puts("1 * 1 = #{1 * 1}")

# Division
puts("1 / 1 = #{1 / 1}")

# Remainder
puts("11 % 3 = #{11 % 3}")

# Exponent
puts("2 ** 3 = #{2 ** 3}")

# Comparison - Equal
puts("1 == 1 = #{1 == 1}")

# Comparison - Not Equal
puts("1 != 1 = #{1 != 1}")

# Comparison - Greater Than
puts("1 > 1 = #{1 > 1}")

# Comparison - Less Than
puts("1 < 1 = #{1 < 1}")

# Comparison - Greater Than or Equal
puts("1 >= 1 = #{1 >= 1}")

# Comparison - Less Than or Equal
puts("1 <= 1 = #{1 <= 1}")

# Comparison - Spaceship
puts("1 <=> 1 = #{1 <=> 1}")
# What is spaceship?
# It is a comparison operator that returns -1, 0, or 1
# depending on whether the first operand is less than, equal to, or greater than the second operand.